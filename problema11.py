lista_original = [1, 1, 2, 3, 4, 4, 5, 1]
# Con set se crea un conjunto a partir de la lista original eliminando los duplicados
conjunto_procesado = set(lista_original)
# Con list se conviere el conjunto nuevamente a una lista
lista_procesada = list(conjunto_procesado)
# Imprimir la lista procesada
# print(lista_procesada)
print(f"Lista_esperada = {lista_procesada}")